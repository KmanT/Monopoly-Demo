/**<h1>guiElements Package</h1>
 * This package includes all of the different classes for all of the classes
 * that create the UI for the program.
 */ 
package cis.monopoly.guiElements;

/**
 * <h2>MainMenu.fxml</h2>
 * This is the fxml file that defines the main menu. There is a label that
 * introduces the game, a button to start a new game, and a button to exit.
 * Accessed by the Controller class.
 * 
 * <h2>HowManyPlayers.fxml</h2>
 * This fxml file is used to define the menu to select the number of players to
 * play. The first button is player versus computer, the second is two players,
 * the third is for three players, and then the fourth activates all four
 * players.
 * 
 * <h2>ALertBox</h2>
 * This is used to inform the user of important information or if their is an
 * error that has occurred.
 * 
 * <h2>ConfirmBox</h2>
 * This is used for the player to make a decision. If they press yes, the
 * confirm box will return true, and if no then the confirm box will return
 * false.
 * 
 * <h2>Controller</h2>
 * This is used to connect all of the fxml files to the program and makes those
 * files have functionality.
 *
 * <h2>GamePlayScene</h2>
 * This class defines the window where the game is played.
 * 
 * @version 0.6
 * @author Kyle Turske
 */
